package Ch00;	// package : 폴더 개념, 패키지 구별코드

//programming language
// pro(미리, 사전의) + gram(쓰다, 적다)
		
// 사람(자연어)       -       컴퓨터(기계어)
//            컴파일러(통역관)
		
